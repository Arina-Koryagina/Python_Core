# Начальный вклад в банке равен 1000 грн. Через каждый месяц размер
# вклада увеличивается на P процентов от имеющейся суммы (P — вещест-
# венное число, 0 < P < 25). По данному P определить, через сколько меся-
# цев размер вклада превысит 1100 грн., и вывести найденное количество
# месяцев K (целое число) и итоговый размер вклада S (вещественное число).

P = float(input("Введіть відсоток P (1-25%) = "))  # 1
K = 0  # 10
S = 1000  # 1104.62
add = 0
while S <= 1100:
    add = S / 100 * P
    S += add
    K += 1
print(f"Розмір вкладу перевищить 1100 грн. за {K} місяців і буде дорівнювати {round(S, 2)} грн.")

