# Дано целое число N (> 1). Если оно является простым, то есть не
# имеет положительных делителей, кроме 1 и самого себя, то вывести True,
# иначе вывести False.

N = int(input("Введіть ціле додатнє число N (> 1): "))
n = 1
deviders = 0
while n <= N:
    if N % n == 0:
        deviders += 1
    n += 1
